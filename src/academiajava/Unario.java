package academiajava;

public class Unario {

	public static void main(String[] args) {
		int result = +1;
		System.out.println(result);
		
		result--;
		System.out.println(result);
		
		result++;
		System.out.println(result);
		
		result = -result;
		System.out.println(result);
		
		boolean sucess = false;
		System.out.println(sucess);
		System.out.println(!sucess);
	}
}
