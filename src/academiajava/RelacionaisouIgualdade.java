package academiajava;

public class RelacionaisouIgualdade {

	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;
		
		if(value1 == value2)
			System.out.println(false);
		
		if(value1 != value2)
			System.out.println(true);
		
		if(value1 > value2)
			System.out.println(false);
		
		if(value1 < value2)
			System.out.println(true);
		
		if(value1 <= value2)
			System.out.println(true);
	}
}
